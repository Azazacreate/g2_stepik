public class i24_Float_Double {
    public static void main(String[] args) {
        // double
        System.out.println("double: " + 3.14 * 10.8 * 10.8);

        // float
        float radius = 10.8f;
        float pi = 3.14f;
        float area = pi * radius * radius;
        System.out.println("area: " + area);


        // hw
        // посчитать возраст:средней_семьи:вашей
        byte gleb = 23;
        float sanal = 23f;
        short nick = 45;
        System.out.println((gleb + sanal + nick) / 3);
    }
}
